; Calcul de la postion

bcl_sub
  exx:ld e,(hl):inc hl:ld d,(hl):inc hl:inc hl:inc hl:ld c,(hl):inc hl:ld b,(hl):inc hl:inc hl:inc hl:exx

repeat 26
  ld c,(ix):inc ix:ld b,(ix):inc ix:ld a,c:sub (hl):inc hl:ld e,a:ld a,b:sbc (hl):inc hl:and &03:or texture/256:ld d,a:ld a,(de):exx:ld (de),a:inc e:ld (bc),a:inc c:exx
rend

  ld c,(ix):inc ix:ld b,(ix):inc ix:ld a,c:sub (hl):inc hl:ld e,a:ld a,b:sbc (hl):inc hl:and &03:or texture/256:ld d,a:ld a,(de):exx:ld (de),a:inc de:ld (bc),a:inc bc:exx

repeat 5
  ld c,(ix):inc ix:ld b,(ix):inc ix:ld a,c:sub (hl):inc hl:ld e,a:ld a,b:sbc (hl):inc hl:and &03:or texture/256:ld d,a:ld a,(de):exx:ld (de),a:inc e:ld (bc),a:inc c:exx
rend

sauve_a_sub  
  ld a,0
  dec a
  ld (sauve_a_sub+1),a
  jp nz,bcl_sub

; Page Flipping

  ld a,(reg12+1)
  xor %00010000
  ld (reg12+1),a

flip
  ld a,0
  xor 1
  ld (flip+1),a
  jp nz,ECR8000

; ecran en &8000

  ld hl,SCRC000
  jp flip_suite

; ecran en &C000

ECR8000
  ld hl,SCR8000

flip_suite
  ld (scr_aff+1),hl

  ret
  
